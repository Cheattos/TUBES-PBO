            String sql = "INSERT INTO tb_biaya VALUES ('"+cbKodeMontir.getSelectedItem()+"','"+txtKodeKonsumen.getText()+"','"+txtKodeMobil.getText()+"','"+txtKodeTransaksi.getText()+"','"+txtTanggalTransaksi.getText()+"','"+txtTotalTransaksi.getText()+"')";
            java.sql.Connection conn = (Connection)koneksi.getkoneksi();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
