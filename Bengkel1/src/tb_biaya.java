    private void tabelBiayaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelBiayaMouseClicked
        // TODO add your handling code here:
        int baris = tabelBiaya.rowAtPoint(evt.getPoint());
        String jab = tabelBiaya.getValueAt(baris, 1).toString();
        cbKodeMontir.setSelectedItem(jab);
        
        String ksmn = tabelBiaya.getValueAt(baris, 2).toString();
        txtKodeKonsumen.setText(ksmn);
        
        String mobil = tabelBiaya.getValueAt(baris, 3).toString();
        txtKodeMobil.setText(mobil);
        
        String kdtr = tabelBiaya.getValueAt(baris, 4).toString();
        txtKodeTransaksi.setText(kdtr);
        
        String tgltr = tabelBiaya.getValueAt(baris, 5).toString();
        txtTanggalTransaksi.setText(tgltr);
        
        String ttr = tabelBiaya.getValueAt(baris, 6).toString();
        txtTotalTransaksi.setText(ttr);
    }//GEN-LAST:event_tabelBiayaMouseClicked

