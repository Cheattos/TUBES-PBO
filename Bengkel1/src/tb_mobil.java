    private void tabelMobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMobilMouseClicked
        // TODO add your handling code here:
        int baris = tabelMobil.rowAtPoint(evt.getPoint());
        String mb = tabelMobil.getValueAt(baris, 1).toString();
        txtKodeMobil.setText(mb);
        
        String nmb = tabelMobil.getValueAt(baris, 2).toString();
        txtNamaMobil.setText(nmb);
        
        String ksmn = tabelMobil.getValueAt(baris, 3).toString();
        txtKodeKonsumen.setText(ksmn);
        
        String pm = tabelMobil.getValueAt(baris, 4).toString();
        txtPlatMobil.setText(pm);
        
        String krs = tabelMobil.getValueAt(baris, 5).toString();
        txtKerusakan.setText(krs);
        
        
    }//GEN-LAST:event_tabelMobilMouseClicked

